def greet_user(name):
    """
    Function to greet a user by name.
    
    Args:
        name (str): Name of the user to greet
        
    Returns:
        str: Greeting message
    """
    return f"Hello, {name}! Welcome to our Git Flow project."


def calculate_sum(a, b):
    """
    Calculate the sum of two numbers.
    
    Args:
        a (int/float): First number
        b (int/float): Second number
        
    Returns:
        int/float: Sum of the two numbers
    """
    return a + b
